def main():
    """Main execution function."""
    print("=" * 60)
    print("Portfolio Visualization Generator")
    print("=" * 60)

    # Check if data files exist
    required_files = [
        "capture_stats.csv",
        "summary_stats.csv",
        "cumulative_returns.csv",
        "weights_used.csv"
    ]

    missing = []
    for fname in required_files:
        if not os.path.exists(os.path.join(HTML_DIR, fname)):
            missing.append(fname)

    if missing:
        print("\n‚ùå ERROR: Missing required data files:")
        for fname in missing:
            print(f"   - {HTML_DIR}/{fname}")
        print("\nPlease run analyze.py first to generate the data.")
        return 1

    # Generate visualizations
    charts_ok = generate_charts()
    if not charts_ok:
        print("\n‚ö†Ô∏è  Charts generation failed, but continuing with HTML...")

    # Generate HTML dashboard
    html_ok = generate_html_dashboard()

    if html_ok:
        print("\n" + "=" * 60)
        print("‚úì Visualization complete!")
        print(f"  Open: {HTML_DIR}/index.html")
        print("=" * 60)
        return 0
    else:
        print("\n‚ùå HTML generation failed")
        return 1


if __name__ == "__main__":
    exit(main())
